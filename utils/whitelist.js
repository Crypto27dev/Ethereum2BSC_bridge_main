export const customWiteToast = (tx, funToast, chain) => {
  if (tx) {
    let CustomToastWithLink = () => (
      <div>
        <h3 style={{ fontSize: "16px" }}>{"Confirming Transaction"}</h3>
        <Link
          legacyBehavior
          href={
            chain == 56
              ? `https://bscscan.com//tx/${tx.hash}`
              : `https://etherscan.io/tx/${tx.hash}`
          }
        >
          <a target="_blank">View in Explorer</a>
        </Link>
      </div>
    );
    const toastId = toast.info(CustomToastWithLink, {
      autoClose: false,
      position: "top-right",
      theme: "dark",
      progress: undefined,
    });
    const intervalId = setInterval(async () => {
      try {
        const receipt = await tx.wait();
        clearInterval(intervalId);
        toast.update(toastId, {
          render: () => (
            <div>
              <h3 style={{ fontSize: "16px" }}>Bridge Successful: </h3>
              <Link
                legacyBehavior
                href={
                  chain == 56
                    ? `https://bscscan.com/tx/${tx.hash}`
                    : `https://etherscan.io/tx/${tx.hash}`
                }
              >
                <a target="_blank">View in Explorer</a>
              </Link>
            </div>
          ),
          autoClose: 3000,
        });
      } catch (error) {
        console.log(error);
      }
    }, 3000);
  }
};

